export function getDateFrom(period: string): string | null {
  const now = new Date();
  switch (period) {
    case "year":  return new Date(now.getFullYear(), 0, 1).toISOString();
    case "6m":    return new Date(now.getTime() - 180 * 86400000).toISOString();
    case "3m":    return new Date(now.getTime() - 90 * 86400000).toISOString();
    case "1m":    return new Date(now.getFullYear(), now.getMonth(), 1).toISOString();
    case "week":  return new Date(now.getTime() - 7 * 86400000).toISOString();
    case "q1":    return new Date(now.getFullYear(), 0, 1).toISOString();
    case "q2":    return new Date(now.getFullYear(), 3, 1).toISOString();
    case "q3":    return new Date(now.getFullYear(), 6, 1).toISOString();
    case "q4":    return new Date(now.getFullYear(), 9, 1).toISOString();
    default:      return null;
  }
}

